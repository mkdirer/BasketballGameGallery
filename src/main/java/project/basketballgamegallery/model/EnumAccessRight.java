package project.basketballgamegallery.model;

/**
 * The EnumAccessRight enum represents the different access rights in the basketball game gallery project.
 * It includes access rights for users, moderators, and administrators.
 */
public enum EnumAccessRight {
    /**
     * User access right.
     */
    ROLE_USER,

    /**
     * Moderator access right.
     */
    ROLE_MODERATOR,

    /**
     * Administrator access right.
     */
    ROLE_ADMIN
}
